package com.kybaby.operationbussiness.redpacket.domain;

public class RedPacketSet implements java.io.Serializable {

	private static final long serialVersionUID = -3392474870831162563L;
	private Long id; //主键
	private String grantType; //发放方式：定额发放、随机发放
	private String quotaValue; //定额值
	private String minSection; //随机发放的区间最小值
	private String maxSection; //随机发放的区间最大值
	private String totalAmount; //发放总金额
	private String surplusAmount; //剩余总金额
	private String isEnabled; //是否生效（Y：是   N：否）
	private String remark; //备注
	
	public final static String GRANTTYPE_QUOTA = "定额发放";
	public final static String GRANTTYPE_RANDOM = "随机发放";
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getGrantType() {
		return grantType;
	}
	public void setGrantType(String grantType) {
		this.grantType = grantType;
	}
	public String getQuotaValue() {
		return quotaValue;
	}
	public void setQuotaValue(String quotaValue) {
		this.quotaValue = quotaValue;
	}
	public String getMinSection() {
		return minSection;
	}
	public void setMinSection(String minSection) {
		this.minSection = minSection;
	}
	public String getMaxSection() {
		return maxSection;
	}
	public void setMaxSection(String maxSection) {
		this.maxSection = maxSection;
	}
	public String getTotalAmount() {
		return totalAmount;
	}
	public void setTotalAmount(String totalAmount) {
		this.totalAmount = totalAmount;
	}
	public String getSurplusAmount() {
		return surplusAmount;
	}
	public void setSurplusAmount(String surplusAmount) {
		this.surplusAmount = surplusAmount;
	}
	public String getIsEnabled() {
		return isEnabled;
	}
	public void setIsEnabled(String isEnabled) {
		this.isEnabled = isEnabled;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
