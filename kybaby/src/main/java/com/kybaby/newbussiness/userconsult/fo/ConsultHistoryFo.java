package com.kybaby.newbussiness.userconsult.fo;

import com.kybaby.domain.DoctorInfo;

public class ConsultHistoryFo {
	/**
	 * 最后一次对话内容
	 */
	private String lastConsult;
	/**
	 * 最后一次对话时间
	 */
	private String lastSubmitTime;
	/**
	 * 医生信息
	 */
	private DoctorInfo doctorInfo;
	/**
	 * 未读消息数
	 */
	private long newMesNum;
	/**
	 * 咨询价格
	 */
	private String consultPrice;
	/**
	 * 会话状态
	 */
	private String consultStatus;
	/**
	 * 一组会话记录id
	 */
	private Long logId;
	/**
	 * 是否当前会话（Y/N）
	 */
	private String isCurrentDialog;
	/**
	 * 是否上线（Y/N）
	 */
	private String isOnline;
	/**
	 * 家庭医生服务包id
	 */
	private Long fdPackageId;
	
	public String getLastConsult() {
		return lastConsult;
	}
	public void setLastConsult(String lastConsult) {
		this.lastConsult = lastConsult;
	}
	public String getLastSubmitTime() {
		return lastSubmitTime;
	}
	public void setLastSubmitTime(String lastSubmitTime) {
		this.lastSubmitTime = lastSubmitTime;
	}
	public DoctorInfo getDoctorInfo() {
		return doctorInfo;
	}
	public void setDoctorInfo(DoctorInfo doctorInfo) {
		this.doctorInfo = doctorInfo;
	}
	public long getNewMesNum() {
		return newMesNum;
	}
	public void setNewMesNum(long newMesNum) {
		this.newMesNum = newMesNum;
	}
	public String getConsultPrice() {
		return consultPrice;
	}
	public void setConsultPrice(String consultPrice) {
		this.consultPrice = consultPrice;
	}
	public String getConsultStatus() {
		return consultStatus;
	}
	public void setConsultStatus(String consultStatus) {
		this.consultStatus = consultStatus;
	}
	public Long getLogId() {
		return logId;
	}
	public void setLogId(Long logId) {
		this.logId = logId;
	}
	public String getIsCurrentDialog() {
		return isCurrentDialog;
	}
	public void setIsCurrentDialog(String isCurrentDialog) {
		this.isCurrentDialog = isCurrentDialog;
	}
	public Long getFdPackageId() {
		return fdPackageId;
	}
	public void setFdPackageId(Long fdPackageId) {
		this.fdPackageId = fdPackageId;
	}
	public String getIsOnline() {
		return isOnline;
	}
	public void setIsOnline(String isOnline) {
		this.isOnline = isOnline;
	}
}
