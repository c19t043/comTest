public class HealthPath implements java.io.Serializable {

	// Fields

	private Long id;
	private String healthPathNum;
	private String healthPathName;
	private String comments;
	private String healthPathStatus;

	// Constructors

	/** default constructor */
	public HealthPath() {
	}

	/** full constructor */
	public HealthPath(String healthPathNum, String healthPathName,
			String comments, String healthPathStatus) {
		this.healthPathNum = healthPathNum;
		this.healthPathName = healthPathName;
		this.comments = comments;
		this.healthPathStatus = healthPathStatus;
	}

	// Property accessors

	public Long getId() {
		return this.id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getHealthPathNum() {
		return this.healthPathNum;
	}

	public void setHealthPathNum(String healthPathNum) {
		this.healthPathNum = healthPathNum;
	}

	public String getHealthPathName() {
		return this.healthPathName;
	}

	public void setHealthPathName(String healthPathName) {
		this.healthPathName = healthPathName;
	}

	public String getComments() {
		return this.comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}

	public String getHealthPathStatus() {
		return this.healthPathStatus;
	}

	public void setHealthPathStatus(String healthPathStatus) {
		this.healthPathStatus = healthPathStatus;
	}

}