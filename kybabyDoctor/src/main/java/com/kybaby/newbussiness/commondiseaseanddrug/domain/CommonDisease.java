package com.kybaby.newbussiness.commondiseaseanddrug.domain;

/**
 * 常见病
 * CommonDisease entity. @author MyEclipse Persistence Tools
 */

public class CommonDisease implements java.io.Serializable {
	private static final long serialVersionUID = 1L;
	private Long id;
	private Long classificationId;
	private String allName;
	private String shortName;
	private String describtion;

	// Constructors

	/** default constructor */
	public CommonDisease() {
	}

	/** full constructor */
	public CommonDisease(Long classificationId, String allName,
			String shortName, String describtion) {
		this.classificationId = classificationId;
		this.allName = allName;
		this.shortName = shortName;
		this.describtion = describtion;
	}

	// Property accessors

	public Long getId() {
		return this.id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getClassificationId() {
		return this.classificationId;
	}

	public void setClassificationId(Long classificationId) {
		this.classificationId = classificationId;
	}

	public String getAllName() {
		return this.allName;
	}

	public void setAllName(String allName) {
		this.allName = allName;
	}

	public String getShortName() {
		return this.shortName;
	}

	public void setShortName(String shortName) {
		this.shortName = shortName;
	}

	public String getDescribtion() {
		return this.describtion;
	}

	public void setDescribtion(String describtion) {
		this.describtion = describtion;
	}

}