package com.kybaby.newbussiness.medicalorgandbusiness.fo;

public class OrderCountInfo {
	/**
	 * 总人数
	 */
	private Integer allSum;
	/**
	 * 已预约人数
	 */
	private Integer bookingSum;
	/**
	 * 已预检人数
	 */
	private Integer havePreSum;
	/**
	 * 已登记人数
	 */
	private Integer registeredSum;
	/**
	 * 已完成人数
	 */
	private Integer finishedSum;
	/**
	 * 已会面人数
	 */
	private Integer meetingSum;
	public Integer getAllSum() {
		return allSum;
	}
	public void setAllSum(Integer allSum) {
		this.allSum = allSum;
	}
	public Integer getHavePreSum() {
		return havePreSum;
	}
	public void setHavePreSum(Integer havePreSum) {
		this.havePreSum = havePreSum;
	}
	public Integer getRegisteredSum() {
		return registeredSum;
	}
	public void setRegisteredSum(Integer registeredSum) {
		this.registeredSum = registeredSum;
	}
	public Integer getFinishedSum() {
		return finishedSum;
	}
	public void setFinishedSum(Integer finishedSum) {
		this.finishedSum = finishedSum;
	}
	public Integer getBookingSum() {
		return bookingSum;
	}
	public void setBookingSum(Integer bookingSum) {
		this.bookingSum = bookingSum;
	}
	public Integer getMeetingSum() {
		return meetingSum;
	}
	public void setMeetingSum(Integer meetingSum) {
		this.meetingSum = meetingSum;
	}
}
