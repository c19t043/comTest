package com.kybaby.newbussiness.doctoroperateflow.domain;

/**
 * 警示内容配置信息
 * @author lihao
 *
 */

public class WarningConfig implements java.io.Serializable {

	// Fields

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/**
	 * 主键
	 */
	private Long wearingId;
	/**
	 * 名称
	 */
	private String wearName;
	/**
	 * 性别（男/女/全部）
	 */
	private String sex;
	/**
	 * 最小年龄
	 */
	private String minAge;
	/**
	 * 最大年龄
	 */
	private String maxAge;
	/**
	 * 是否判断年龄（Y/N）
	 */
	private String isJudgeAge;
	/**
	 * 是否判断性别
	 */
	private String isJudgeSex;
	/**
	 * 普通参考值
	 */
	private String normalVal;
	/**
	 * 最小值（数字）
	 */
	private String minVal;
	/**
	 * 最大值（数字）
	 */
	private String maxVal;
	/**
	 * 最大值提醒
	 */
	private String maxValWear;
	/**
	 * 最小值提醒
	 */
	private String minValWear;
	/**
	 * 一般提醒
	 */
	private String generalWear;

	// Constructors

	/** default constructor */
	public WarningConfig() {
	}

	/** full constructor */
	public WarningConfig(String wearName, String sex, String minAge,
			String maxAge, String isJudgeAge, String isJudgeSex,
			String normalVal, String minVal, String maxVal, String maxValWear,
			String minValWear, String generalWear) {
		this.wearName = wearName;
		this.sex = sex;
		this.minAge = minAge;
		this.maxAge = maxAge;
		this.isJudgeAge = isJudgeAge;
		this.isJudgeSex = isJudgeSex;
		this.normalVal = normalVal;
		this.minVal = minVal;
		this.maxVal = maxVal;
		this.maxValWear = maxValWear;
		this.minValWear = minValWear;
		this.generalWear = generalWear;
	}

	// Property accessors

	public Long getWearingId() {
		return this.wearingId;
	}

	public void setWearingId(Long wearingId) {
		this.wearingId = wearingId;
	}

	public String getWearName() {
		return this.wearName;
	}

	public void setWearName(String wearName) {
		this.wearName = wearName;
	}

	public String getSex() {
		return this.sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getMinAge() {
		return this.minAge;
	}

	public void setMinAge(String minAge) {
		this.minAge = minAge;
	}

	public String getMaxAge() {
		return this.maxAge;
	}

	public void setMaxAge(String maxAge) {
		this.maxAge = maxAge;
	}

	public String getIsJudgeAge() {
		return this.isJudgeAge;
	}

	public void setIsJudgeAge(String isJudgeAge) {
		this.isJudgeAge = isJudgeAge;
	}

	public String getIsJudgeSex() {
		return this.isJudgeSex;
	}

	public void setIsJudgeSex(String isJudgeSex) {
		this.isJudgeSex = isJudgeSex;
	}

	public String getNormalVal() {
		return this.normalVal;
	}

	public void setNormalVal(String normalVal) {
		this.normalVal = normalVal;
	}

	public String getMinVal() {
		return this.minVal;
	}

	public void setMinVal(String minVal) {
		this.minVal = minVal;
	}

	public String getMaxVal() {
		return this.maxVal;
	}

	public void setMaxVal(String maxVal) {
		this.maxVal = maxVal;
	}

	public String getMaxValWear() {
		return this.maxValWear;
	}

	public void setMaxValWear(String maxValWear) {
		this.maxValWear = maxValWear;
	}

	public String getMinValWear() {
		return this.minValWear;
	}

	public void setMinValWear(String minValWear) {
		this.minValWear = minValWear;
	}

	public String getGeneralWear() {
		return this.generalWear;
	}

	public void setGeneralWear(String generalWear) {
		this.generalWear = generalWear;
	}

}