package com.java.medicalorgandbusiness.orgsetmeal.vo;

import java.io.Serializable;

import com.java.publichealth.residentsfile.vo.KyUserInfo;

/**
 * @author chenjunfan
 * @createTime 2016-07-19
 * @description 咨询宝宝
 */
public class ConsultBabyInfo implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -452436610624194055L;
	
	private Long id;
	private String babyName;
	private String sex;
	private String birthday;
	private String headImg;
	
	private KyUserInfo userInfo;
	
	//页面显示用
	private String monthAge;
	
	public ConsultBabyInfo(){}
	
	public ConsultBabyInfo(String babyName,String sex,String birthday){
		this.babyName =babyName ;
		this.sex = sex;
		this.birthday  = birthday;
	}
	
	public KyUserInfo getUserInfo() {
		return userInfo;
	}
	public void setUserInfo(KyUserInfo userInfo) {
		this.userInfo = userInfo;
	}
	public String getBabyName() {
		return babyName;
	}
	public void setBabyName(String babyName) {
		this.babyName = babyName;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getHeadImg() {
		return headImg;
	}
	public void setHeadImg(String headImg) {
		this.headImg = headImg;
	}
	public String getMonthAge() {
		return monthAge;
	}
	public void setMonthAge(String monthAge) {
		this.monthAge = monthAge;
	}
}
